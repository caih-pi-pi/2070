DATA SEGMENT
    BUF   DB 4 DUP(?)
    MAXV  DB ?
    MINV  DB ?
    MSG1  DB 0DH,0AH,'MAX = $'
    MSG2  DB 0DH,0AH,'MIN = $'
DATA ENDS

STACK SEGMENT STACK
    DW 64 DUP(?)
STACK ENDS

CODE SEGMENT
    ASSUME CS:CODE,DS:DATA,SS:STACK

START:
    MOV AX,DATA
    MOV DS,AX

    MOV CX,4
    LEA SI,BUF

INLOOP:
    MOV AH,01H
    INT 21H
    SUB AL,'0'
    MOV [SI],AL
    INC SI
    LOOP INLOOP

    LEA SI,BUF
    MOV CX,4
    CALL BRANCH

    MOV MAXV,AH
    MOV MINV,AL

    LEA DX,MSG1
    MOV AH,09H
    INT 21H

    MOV DL,MAXV
    ADD DL,'0'
    MOV AH,02H
    INT 21H

    LEA DX,MSG2
    MOV AH,09H
    INT 21H

    MOV DL,MINV
    ADD DL,'0'
    MOV AH,02H
    INT 21H

    MOV AX,4C00H
    INT 21H

BRANCH PROC NEAR
    PUSH BX
    PUSH CX
    PUSH SI

    MOV AL,[SI]
    MOV AH,AL
    INC SI
    DEC CX

A1:
    JCXZ A4
    MOV BL,[SI]

    CMP BL,AH
    JBE A2
    MOV AH,BL

A2:
    CMP BL,AL
    JAE A3
    MOV AL,BL

A3:
    INC SI
    LOOP A1

A4:
    POP SI
    POP CX
    POP BX
    RET
BRANCH ENDP

CODE ENDS
END START